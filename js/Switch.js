//alternative to multiple if-else statements when dealing with multiple conditions.
let color = "blue";

switch(color){
    case 'red':
        console.log('Choosed Color is red');
        break;
    case 'blue':
        console.log('Choosed Color is blue');
        break;
    case 'green':
        console.log('Choosed Color is green');
        break;
    default:
        console.log('Color is not red, blue, or green');     
}